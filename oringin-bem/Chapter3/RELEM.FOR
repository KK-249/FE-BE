      SUBROUTINE RELEM
C
C---- READ IN ELEMENT CONNECTIVITY
C
      COMMON /CONTR/NNODE,NELEM
      COMMON /ELEM/KIND(2000),NODE(4,2000)
      READ(5,*) NELEM,NREC
      NE=1
      NUMBER=0
      DO 50 NR=1,NREC
        NE=NE+NUMBER
        READ(5,*) NODEI,NODEL,NUMBER,KINDI
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C                                                                      C
C     NELEM =NUMBER OF ELEMENTS                                        C
C     NREC =NUMBER OF ELEMENT DATA RECORDS                             C
C     NODEI =NODAL POINT NUMBER FOR THE FIRST NODE IN CURRENT RECORD   C
C     NODEL =NODAL POINT NUMBER FOR THE LAST NODE IN CURRENT RECORD    C
C     NUMBER =NUMBER OF ELEMENTS TO BE GENERATED IN THIS RECORD        C
C     KINDI =1     LINEAR ELEMENTS                                     C
C           =2     QUADRATIC ELEMENTS                                  C
C           =3     CUBIC ELEMENTS                                      C
C                                                                      C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
        IF(KINDI.LT.1.OR.KINDI.GT.3) GO TO 99
        IF(NODEI.LT.1.OR.NODEI.GT.NNODE) GO TO 99
        IF(NUMBER.LT.1.OR.(NE+NUMBER-1).GT.200) GO TO 99
        INCREM=0
        DO 40 I=NE,NE+NUMBER-1
          KIND(I)=KINDI
          N1=NODEI+INCREM
          DO 20 J=1,KINDI+1
            NODE(J,I)=N1+J-1
20        CONTINUE
          INCREM=INCREM+KINDI
40      CONTINUE
        IF(NODEI.GE.NODEL) NODE(KINDI+1,NE+NUMBER-1)=NODEL
50    CONTINUE
C
C     PRINT OUT ELEMENT CONNECTIVITY
C
      WRITE(6,55)
55    FORMAT(/2X,'ELEMENT CONNECTIVITY :'/)
      DO 60 I=1,NELEM
        KINDI=KIND(I)
        WRITE(6,200) I,KIND(I),(NODE(J,I),J=1,KINDI+1)
200     FORMAT(5X,'ELEMENT #',I3,3X,'DEGREE=',I3,3X,
     &         'NODES=',4I3)
60    CONTINUE
      RETURN
99    WRITE(6,*) '----ERROR IN ELEMENT DATA-----'
      STOP
      END