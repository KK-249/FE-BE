      SUBROUTINE PREP
C
C---- PREPROCESSOR
C
      COMMON /CHI/NCH,XCH(20),YCH(20)
      COMMON /FPOINTS/NF,XF(100),YF(100)
      CHARACTER*60 TITLE
      CHARACTER*4 BLOCK
      READ(5,100) TITLE
      WRITE(6,100) TITLE

      CALL SETINT
      NCH=0
      NF=0

10    READ(5,200,END=99) BLOCK
      IF(BLOCK.EQ.'GENE') CALL RGENE
      IF(BLOCK.EQ.'NODE') CALL RNODE
      IF(BLOCK.EQ.'ELEM') CALL RELEM
      IF(BLOCK.EQ.'BOUN') CALL BC
      IF(BLOCK.EQ.'FIEL') CALL RFIELD
      IF(BLOCK.EQ.'CHIE') CALL RCHIEF
      IF(BLOCK.EQ.'END ') GO TO 99
      IF(BLOCK.EQ.'SOLV') RETURN
      GO TO 10
99    STOP
100   FORMAT(A60)
200   FORMAT(A4)
      END
