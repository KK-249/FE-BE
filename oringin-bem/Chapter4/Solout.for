	SUBROUTINE SOLOUT
C
C  IDENTIFY THE SOLUTION VECTOR
C
        COMMON /BG/BIG
	COMMON /CONTR/NNODE,NELEM
	COMMON /ELEM/NODE(4,2000)
	COMMON /MATRX/A(2020,2000),B(2020),XS(2000)
	COMMON /SOL/SP(2000),VN(4,2000)
	COMMON /BCC/CA(4,2000),CB(4,2000),CC(4,2000)
        COMPLEX BIG,A,B,XS,SP,VN,CA,CB,CC
C
	DO 100 I=1,NNODE
	  IF(SP(I).EQ.BIG) SP(I)=XS(I)
100	CONTINUE
C
	DO 300 K=1,NELEM
          I34=4
          IF(NODE(3,K).EQ.NODE(4,K)) I34=3
	  DO 200 J=1,I34
	    IF(VN(J,K).NE.BIG) GO TO 200
            NOD=NODE(J,K)
	    IF(CB(J,K).EQ.0.0) THEN
	      VN(J,K)=XS(NOD)
	    ELSE
	      VN(J,K)=(CC(J,K)-CA(J,K)*SP(NOD))/CB(J,K)
            END IF
200       CONTINUE
300     CONTINUE
C
C    PRINT OUT BOUNDARY SOLUTION
C
        WRITE(6,400)
400     FORMAT(//2X,'SOUND PRESSURE ON THE BOUNDARY :'/)
        DO 500 I=1,NNODE
          AMP=CABS(SP(I))
          WRITE(6,450) I,SP(I),AMP
450       FORMAT(3X,'NODE #',I4,2X,'P=',2G12.3,2X,'AMPL=',G15.4)
500     CONTINUE
C
        WRITE(6,550)
550     FORMAT(/2X,'VN ON THE BOUNDARY :'/)
        DO 600 K=1,NELEM
          I34=4
          IF(NODE(3,K).EQ.NODE(4,K)) I34=3
          WRITE(6,580) K,(VN(J,K),J=1,I34)
580       FORMAT(/3X,'ELEMENT #',I4,2X,'VN=',8(1X,E10.2))
600     CONTINUE
        RETURN
        END