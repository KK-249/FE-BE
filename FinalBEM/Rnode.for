      SUBROUTINE RNODE
C
C---- READ IN NODAL POINT DATA
C
      COMMON /CONTR/NNODE,NELEM
      COMMON /NODES/X(3,4000)
      READ(5,*) NNODE
      DO 30 NR=1,NNODE
      READ(5,*) N,X(1,NR),X(2,NR),X(3,NR)
30    CONTINUE
C
C---- PRINT OUT NODAL POINT COORDINATES
C
      WRITE(6,100)
100   FORMAT(/2X,'NODAL POINT COORDINATES:'/)
      DO 102 I=1,NNODE
        WRITE(6,101) I,X(1,I),X(2,I),X(3,I)
101     FORMAT(5X,'NODE #',I4,5X,'X=',F8.3,3X,'Y=',F8.3,3X,'Z=',F8.3)
102   CONTINUE
      RETURN
      END